const extractFirstColor = (value: string): string => {
  const hexMatch = value.match(/#(?:[0-9a-fA-F]{3}){1,2}/)
  if (hexMatch) {
    return hexMatch[0]
  }

  const rgbaMatch = value.match(/rgba?\([^)]*\)/)
  if (rgbaMatch) {
    return rgbaMatch[0]
  }

  return value
}

